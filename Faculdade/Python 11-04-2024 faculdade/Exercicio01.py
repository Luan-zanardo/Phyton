numero1 = int(input("Digite numero 1: "))
numero2 = int(input("Digite numero 2: "))

if(numero1 > numero2):
    print(f' numero 1: {numero1} é maior que numero 2: {numero2}')
elif(numero1 < numero2):
    print(f' numero 2: {numero2} é maior que numero 1: {numero1}')
else:
    print(f' os numero 1: {numero1} e numero 2: {numero2} são iguais')